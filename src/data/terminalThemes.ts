export const DEFAULT_THEME = "Catppuccin Mocha";

export function getTheme(name: string): TerminalTheme {
  return TERMINAL_THEMES.find((t) => t.name === name) ?? TERMINAL_THEMES[0];
}

export function getThemesByCategory(): {
  dark: TerminalTheme[];
  light: TerminalTheme[];
} {
  return {
    dark: TERMINAL_THEMES.filter((t) => t.category === "dark"),
    light: TERMINAL_THEMES.filter((t) => t.category === "light"),
  };
}
